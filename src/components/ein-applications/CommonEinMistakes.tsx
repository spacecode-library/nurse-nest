
import React from "react";

export default function CommonEinMistakes() {
  return (
    <ul className="list-disc ml-6 mb-4">
      <li>Entering your business name differently on EIN, bank, and state registrations—always use the same name</li>
      <li>Using the wrong business address (must be current and match your official records)</li>
      <li>Applying as the wrong entity type (sole proprietor vs. LLC vs. corporation)</li>
      <li>Submitting applications with missing information</li>
      <li>Paying for an EIN through third-party companies—the IRS issues EINs for free</li>
      <li>Losing your confirmation letter (always save a digital + paper copy)</li>
      <li>Requesting more than one EIN per day (IRS limit per responsible party)</li>
    </ul>
  );
}
